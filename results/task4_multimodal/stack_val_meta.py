from __future__ import annotations

import argparse
import json
import os

import numpy as np
import pandas as pd
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import average_precision_score, roc_auc_score


def _load_pred(path: str) -> pd.DataFrame:
    df = pd.read_csv(path)
    need = {"global_id", "y_true", "y_prob"}
    miss = need - set(df.columns)
    if miss:
        raise ValueError(f"{path} missing columns: {sorted(miss)}")
    return df[["global_id", "y_true", "y_prob"]].copy()


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--outdir", default="/root/autodl-fs/lyy/task4_ehr_ct")
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--models", default="catboost,lightgbm,xgboost")
    args = ap.parse_args()

    outdir = args.outdir
    models = [m.strip() for m in args.models.split(",") if m.strip()]

    # For meta training, we use VAL predictions (generated by re-running base models and saving val preds).
    # Currently, our base scripts save test preds only. Here we implement a simple, safe alternative:
    # fit meta on test predictions is NOT allowed. So we only provide averaging as a no-leak ensemble.
    # (To do proper stacking, extend base trainer to also dump val preds.)
    test_dfs = [_load_pred(os.path.join(outdir, f"test_pred_{m}.csv")).rename(columns={"y_prob": f"p_{m}"}) for m in models]
    base = test_dfs[0][["global_id", "y_true"]].copy()
    for d in test_dfs:
        base = base.merge(d.drop(columns=["y_true"]), on="global_id", how="inner")

    y = base["y_true"].astype(int).to_numpy()
    P = base[[f"p_{m}" for m in models]].to_numpy(dtype=float)

    # Safe ensemble: mean probability (no tuning on test)
    p_mean = P.mean(axis=1)
    auc = float(roc_auc_score(y, p_mean))
    pr = float(average_precision_score(y, p_mean))

    out = {
        "method": "mean_ensemble_of_test_probs (no meta training, no leakage)",
        "models": models,
        "test_roc_auc": auc,
        "test_pr_auc": pr,
        "n_test": int(len(y)),
        "note": "如需真正 stacking，请让基模型同时导出 val_pred_*.csv，再用 val 训练 meta。",
    }
    out_path = os.path.join(outdir, "metrics_mean_ensemble.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)
    print(json.dumps(out, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()

